const takeUntil = function(array, callback) {
  // ...
  let output = [];
  let idx;
  for (let item of array) {
    if (callback(item)) {
      idx = array.indexOf(item);
    }
  }
  for (let i = 0; i < idx; i++) {
    output.push(array[i]);
  }
  

  return output;
};

